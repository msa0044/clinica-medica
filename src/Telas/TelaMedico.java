    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int confirmacao = JOptionPane.showConfirmDialog(null, "Deseja concluir, salvar e imprimir a consulta?");
        if (confirmacao == JOptionPane.OK_OPTION) {

            String cod = "";
            int i = 0;
            for (i = jComboBox2.getSelectedItem().toString().length() - 2; !((jComboBox2.getSelectedItem().toString().charAt(i) + "").equals("(")); i--) {
                cod = jComboBox2.getSelectedItem().toString().charAt(i) + cod;
            }
            ConsultasAgendadas cc = new ConsultasAgendadas();
            for (int o = 0; o <= Dados.getListaDeConsultasAgendadas().size() - 1; o++) {
                if (Dados.getListaDeConsultasAgendadas().get(o).getIdConsultaAgendada() == Integer.parseInt(cod)) {
                    cc = Dados.getListaDeConsultasAgendadas().get(o);
                }
            }

            CriarArquivoDeImpressao imprimir = new CriarArquivoDeImpressao(cc, jTextArea1.getText(), false);

            boolean ok = imprimir.getOk();
            if (ok) {
                Consulta c = new Consulta(jTextArea1.getText(), cc.getNomeConsulta(),
                        cc.getDiaConsulta(), cc.getMesConsulta(), cc.getAnoConsulta(),
                        cc.getHoraConsulta(), cc.getMinutosConsulta(), cc.getMedicoResponsavel(),
                        cc.getPacienteAcompanhado());
                
                DAOConsulta.salvar(c);
                DAOConsultasAgendadas.remover(cc);
            }

            TelaMedico t = new TelaMedico(this.medico);
            t.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

