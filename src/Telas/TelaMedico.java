    public void converterVectorConsulta() {
        ArrayList<ConsultasAgendadas> lista = Dados.ListaDeConsultasAgendadas;
        String[] listaDeNomes = new String[lista.size()];
            int y = 0;
        for (int i = 0; i <= lista.size() - 1; i++) {
            ConsultasAgendadas cc = lista.get(i);
            if (cc.getMedicoResponsavel().getIdMedico() == this.medico.getIdMedico()) {
                listaDeNomes[y] = ("Consulta: " + cc.getNomeConsulta() + " ("
                        + cc.getDiaConsulta() + "/" + cc.getMesConsulta() + "/"
                        + cc.getAnoConsulta() + "   -   " + cc.getHoraConsulta() + ":"
                        + cc.getMinutosConsulta() + ")"
                        + ": Paciente: " + cc.getPacienteAcompanhado().getNome() + " "
                        + cc.getPacienteAcompanhado().getIdPaciente()
                        + "; ("
                        + cc.getIdConsultaAgendada() + ")");
                
            jComboBox2.addItem(listaDeNomes[y]);
                y++;
            }
        }
    }

