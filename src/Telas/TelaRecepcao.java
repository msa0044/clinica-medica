    public String[] converterVectorConsulta(ArrayList<ConsultasAgendadas> lista) {
        String[] listaDeNomes = new String[lista.size() + 20];
        for (int i = 0; i <= lista.size() - 1; i++) {
            listaDeNomes[i] = ("Consulta: " + lista.get(i).getNomeConsulta()
                    + ": Paciente: " + lista.get(i).getPacienteAcompanhado().getNome() + " "
                    + lista.get(i).getPacienteAcompanhado().getIdPaciente()
                    + "; Médico: " + lista.get(i).getMedicoResponsavel().getNome()
                    + " " + lista.get(i).getMedicoResponsavel().getIdMedico() + "; ("
                    + lista.get(i).getIdConsultaAgendada() + ")");
        }
        return listaDeNomes;
    }

