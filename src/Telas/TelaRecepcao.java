    private void listamedicoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listamedicoValueChanged
        String cod = "";
        int i = 0;
        for (i = listamedico.getSelectedValue().length() - 2; !((listamedico.getSelectedValue().charAt(i) + "").equals("(")); i--) {
            cod = listamedico.getSelectedValue().charAt(i) + cod;
        }

        Medico m = new Medico();
        for (int o = 0; o <= Dados.getListaDeMedicos().size() - 1; o++) {
            if (Dados.getListaDeMedicos().get(o).getIdMedico() == Integer.parseInt(cod)) {
                m = Dados.getListaDeMedicos().get(o);
            }
        }
        text3.setText(m.getNome());
        text4.setText(m.getCPF());
        text5.setText(m.getTelefone());
    }//GEN-LAST:event_listamedicoValueChanged

